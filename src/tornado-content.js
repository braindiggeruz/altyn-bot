// Resolve effective text for a given segment+day
export function resolveTornadoText(day, segment) {
  if (!day) return '';
  if (day.text) return day.text;
  if (day.bySegment) {
    const seg = (segment && day.bySegment[segment]) ? segment : 'generic';
    return day.bySegment[seg] || day.bySegment.generic || '';
  }
  return '';
}

